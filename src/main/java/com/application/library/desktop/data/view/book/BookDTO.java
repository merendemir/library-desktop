package com.application.library.desktop.data.view.book;


import com.application.library.desktop.data.view.core.IntegerEntityDTO;
import com.application.library.desktop.data.view.shelf.ShelfDTO;

public class BookDTO extends IntegerEntityDTO {
    private String name;
    private String author;
    private String isbn;
    private Integer pageCount;
    private String publisher;
    private String publishedAt;
    private String language;
    private String description;
    private String imageUrl;
    private Integer totalCount;
    private Integer availableCount;
    private ShelfDTO shelf;

    public BookDTO() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public Integer getPageCount() {
        return pageCount;
    }

    public void setPageCount(Integer pageCount) {
        this.pageCount = pageCount;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    public String getPublishedAt() {
        return publishedAt;
    }

    public void setPublishedAt(String publishedAt) {
        this.publishedAt = publishedAt;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public Integer getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(Integer totalCount) {
        this.totalCount = totalCount;
    }

    public Integer getAvailableCount() {
        return availableCount;
    }

    public void setAvailableCount(Integer availableCount) {
        this.availableCount = availableCount;
    }

    public ShelfDTO getShelf() {
        return shelf;
    }

    public void setShelf(ShelfDTO shelf) {
        this.shelf = shelf;
    }
}
